import os
from dotenv import load_dotenv
from gigachat import GigaChat

load_dotenv()

GIGACHAT_TOKEN = os.getenv("GIGACHAT_TOKEN")

client = GigaChat(credentials=f"{GIGACHAT_TOKEN}", verify_ssl_certs=False)

# Промпт повышенной точности для работы с тех. документацией
PROMPT = """
Ты — система извлечения данных из тендерных документов/эксперт по техническому аудиту и государственной стандартизации (ГОСТ, ТУ, СанПиН).
Твоя задача: провести глубокую расшифровку позиций закупки.

Верни СТРОГО JSON. Без текста до или после.

ИНСТРУКЦИИ ПО ИДЕНТИФИКАЦИИ:
1. Если позиция содержит артикул, код или ГОСТ (например, "ГОСТ 4045-75"), найди в своих знаниях точные характеристики этого стандарта и опиши их в поле "description".
2. Если указан "Набор" или "Комплект", расшифруй его полный состав согласно типовым отраслевым спецификациям (например, для электротехники, сантехники или робототехники).
3. В поле "technical_standards" выдели все найденные или подразумеваемые нормативные акты (ГОСТ, ТУ, регламенты).
4. Если товар является уникальным или заказным, укажи это, проанализировав характеристики из текста.

Если данных нет — используй null или [].

ИЗВЛЕКАЙ:
- оборудование (названия товаров)
- характеристики (если есть)
- контакты (email, телефон, адрес, организация)

ИГНОРИРУЙ:
- законы
- общие условия
- юридический текст


Формат ответа:
{
  "items": [
    {
      "equipment_name": "Название из документа",
      "equipment_type": "Промышленная категория",
      "description": "МАКСИМАЛЬНО ТОЧНО: Состав набора, техническое назначение, расшифровка аббревиатур и артикулов",
      "technical_standards": ["ГОСТ XXXX-XX", "ТУ..."],
      "manufacturer": null,
      "model": null,
      "key_specs": {
        "параметр": "значение"
      }
    }
  ],
  "contacts": {
    "organization": "",
    "contact_person": "",
    "email": "",
    "phone": "",
    "address": ""
  }
}

ТЕКСТ ТЕНДЕРА:
"""

def extract_data_from_chunk(chunk):
    # Прямая передача чанка для глубокого анализа
    response = client.chat(PROMPT + chunk)
    return response.choices[0].message.content